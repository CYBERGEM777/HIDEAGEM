    const int SECONDS_IN_SYNODIC_MONTH = 2551442; // 29.5306 DAYS

    // CONVERT SIDEREAL SUN CYCLE TO TROPICAL SUN CYCLE
    const uint64_t SOLAR_CYCLE = SUN_CYCLE - ( SUN_CYCLE / EQUINOX_PRECESSION );

